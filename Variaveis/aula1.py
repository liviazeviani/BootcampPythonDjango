#Tipo inteiro
numero = int(input('Digite um número: '))
print('O número digitado foi ' + str(numero))

#Tipo float (com vígula)
numero2 = float(input('Digite um número float: '))
print('O número digitado foi ' + str(numero2))

#Tipo string

nome = str(input('Digite seu nome: '))
#não é necessário indicar que é um string, por padrão ele entende que é uma string
print('Seu nome é ' + nome)
