#Operações matemáticas

#Soma

numero1 = int(input('Insira um número: '))
numero2 = int(input('Insira outro número: '))

soma = numero1 + numero2

print ('A soma desses dois números é: ', (soma))

#Subtração

sub = numero2 - numero1
print ('A subtração desses dois números é: ', (sub))

#Multiplicação

mult = numero2 * numero1
print ('multiplicação desses dois números é: ', (mult))

#Divisão

div = numero1/numero2
print ('A divisão desses dois números é: ', (div))

#Divisão inteira

divInteira = numero1//numero2
print('A divisão inteira desses dois números é: ', (divInteira))

#Incremento

